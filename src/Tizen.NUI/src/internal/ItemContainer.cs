                    return Current;
                }
            }

            /// <since_tizen> 3 </since_tizen>
            public bool MoveNext()
            {
                int size = collectionRef.Count;
                bool moveOkay = (currentIndex + 1 < size) && (size == currentSize);
                if (moveOkay)
                {
                    currentIndex++;
                    currentObject = collectionRef[currentIndex];
                }
                else
                {
                    currentObject = null;
                }
                return moveOkay;
            }

            /// <since_tizen> 3 </since_tizen>
            public void Reset()
            {
