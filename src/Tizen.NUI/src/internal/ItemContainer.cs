                currentObject = null;
                if (collectionRef.Count != currentSize)
                {
                    throw new global::System.InvalidOperationException("Collection modified.");
                }
            }

            /// <since_tizen> 3 </since_tizen>
            public void Dispose()
            {
                currentIndex = -1;
                currentObject = null;
            }
        }

