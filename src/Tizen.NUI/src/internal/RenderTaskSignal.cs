namespace Tizen.NUI
{
    internal class RenderTaskSignal : Disposable
    {

        internal RenderTaskSignal(global::System.IntPtr cPtr, bool cMemoryOwn) : base(cPtr, cMemoryOwn)
        {
        }


        protected override void ReleaseSwigCPtr(System.Runtime.InteropServices.HandleRef swigCPtr)
        {
            Interop.RenderTask.delete_RenderTaskSignal(swigCPtr);
        }


        public RenderTaskSignal() : this(Interop.RenderTask.new_RenderTaskSignal(), true)
        {
            if (NDalicPINVOKE.SWIGPendingException.Pending) throw NDalicPINVOKE.SWIGPendingException.Retrieve();
        }
    }
}
