using System.ComponentModel;

namespace Tizen.NUI.Components
{
    /// <summary>
    /// SelectButtonAttributes is a class which saves SelectButton's ux data.
    /// </summary>
    /// <since_tizen> 6 </since_tizen>
    /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
    [EditorBrowsable(EditorBrowsableState.Never)]
    public class SelectButtonAttributes : ButtonAttributes
    {
        /// <summary>
        /// Creates a new instance of a SelectButtonAttributes.
        /// </summary>
        /// <since_tizen> 6 </since_tizen>
        /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
        [EditorBrowsable(EditorBrowsableState.Never)]
        public SelectButtonAttributes() : base() { }
        /// <summary>
        /// Creates a new instance of a SelectButtonAttributes with attributes.
        /// </summary>
        /// <param name="attributes">Create SelectButtonAttributes by attributes customized by user.</param>
        /// <since_tizen> 6 </since_tizen>
        /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
        [EditorBrowsable(EditorBrowsableState.Never)]
        public SelectButtonAttributes(SelectButtonAttributes attributes) : base(attributes)
        {
            if(attributes == null)
            {
                return;
            }
            if (attributes.SelectableImageAttributes != null)
            {
                SelectableImageAttributes = attributes.SelectableImageAttributes.Clone() as ImageAttributes;
            }

            if (attributes.SelectableImageBackgroundAttributes != null)
            {
                SelectableImageBackgroundAttributes = attributes.SelectableImageBackgroundAttributes.Clone() as ImageAttributes;
            }

            if (attributes.SelectableImageShadowAttributes != null)
            {
                SelectableImageShadowAttributes = attributes.SelectableImageShadowAttributes.Clone() as ImageAttributes;
            }
        }

        /// <summary>
        /// Selectable image's attributes.
        /// </summary>
        /// <since_tizen> 6 </since_tizen>
        /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
        [EditorBrowsable(EditorBrowsableState.Never)]
        public ImageAttributes SelectableImageAttributes
        {
            get;
            set;
        }

        /// <summary>
        /// Background image's attributes.
        /// </summary>
        /// <since_tizen> 6 </since_tizen>
        /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
        [EditorBrowsable(EditorBrowsableState.Never)]
        public ImageAttributes SelectableImageBackgroundAttributes
        {
            get;
            set;
        }

        /// <summary>
        /// Shadow image's attributes.
        /// </summary>
        /// <since_tizen> 6 </since_tizen>
        /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
        [EditorBrowsable(EditorBrowsableState.Never)]
        public ImageAttributes SelectableImageShadowAttributes
        {
            get;
            set;
        }

        /// <summary>
        /// Attributes's clone function.
        /// </summary>
        /// <since_tizen> 6 </since_tizen>
        /// This will be public opened in tizen_5.5 after ACR done. Before ACR, need to be hidden as inhouse API.
        [EditorBrowsable(EditorBrowsableState.Never)]
        public override Attributes Clone()
        {
            return new SelectButtonAttributes(this);
        }
    }
}
